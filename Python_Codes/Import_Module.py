import Fun
print("\t\tSquare")
a=int(input("Enter the Side of Square : "))
print("Area of the Square is ",Fun.Square(a),"sq.units")

print("\n\t\tRectangle")
l=int(input("Enter the Length of Rectangle : "))
w=int(input("Enter the Width of Rectangle : "))
print("Area of the Rectangle is ",Fun.Rectangle(l,w),"sq.units")

print("\n\t\tTriangle")
h=int(input("Enter the Height of Triangle : "))
b=int(input("Enter the Base of Triangle : "))
print("Area of the Square is ",Fun.Triangle(h,b),"sq.units")

print("\n\t\tCircle")
r=int(input("Enter the Radius of Circle : "))
print("Area of the Square is ",Fun.Circle(r),"sq.units")
